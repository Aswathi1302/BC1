public class simplepgm {
    int Tea;
    int Coffe;
    String vada;

    public simplepgm(int tea, int coffe, String vada) {
        Tea = tea;
        Coffe = coffe;
        this.vada = vada;
    }

    public int getTea() {
        return Tea;
    }

    public void setTea(int tea) {
        Tea = tea;
    }

    public int getCoffe() {
        return Coffe;
    }

    public void setCoffe(int coffe) {
        Coffe = coffe;
    }

    public String getVada() {
        return vada;
    }

    public void setVada(String vada) {
        this.vada = vada;
    }

}
