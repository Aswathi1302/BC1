package pkg1;

public class spgm {
    int tea;
    int coffe;

    public int getTea() {
        return tea;
    }

    public void setTea(int tea) {
        this.tea = tea;
    }

    public int getCoffe() {
        return coffe;
    }

    public void setCoffe(int coffe) {
        this.coffe = coffe;
    }

    public int getVada() {
        return vada;
    }

    public void setVada(int vada) {
        this.vada = vada;
    }

    public spgm(int tea, int coffe, int vada) {
        this.tea = tea;
        this.coffe = coffe;
        this.vada = vada;
    }

    int vada;


}
